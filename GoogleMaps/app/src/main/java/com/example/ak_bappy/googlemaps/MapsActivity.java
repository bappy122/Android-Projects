    public void onClickSearch(View v)
    {
        EditText textField = (EditText) findViewById(R.id.textField);
        String location = textField.getText().toString();
        List<Address> addressList = null;

        if (location.equals("") || location == null)
        {
            Toast.makeText(getApplicationContext(), "You must need to enter Location", Toast.LENGTH_SHORT).show();
        }
        else
        {
            Geocoder gcd = new Geocoder(this);
            try
            {
                addressList = gcd.getFromLocationName(location, 1);
            }
            catch (IOException e)
            {
                e.printStackTrace();
            }
            Address address = addressList.get(0);
            LatLng latLng = new LatLng(address.getLatitude(),address.getLongitude());
            mMap.addMarker(new MarkerOptions().position(latLng).title("Marker"));
            mMap.animateCamera(CameraUpdateFactory.newLatLng(latLng));
        }
    }

